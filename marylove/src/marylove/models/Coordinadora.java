public class Coordinadora {
    private int coordinadora_codigo;
    private int personal_codigo;

    public Coordinadora() {
    }

    public int getCoordinadora_codigo() {
        return coordinadora_codigo;
    }

    public void setCoordinadora_codigo(int coordinadora_codigo) {
        this.coordinadora_codigo = coordinadora_codigo;
    }

    public int getPersonal_codigo() {
        return personal_codigo;
    }

    public void setPersonal_codigo(int personal_codigo) {
        this.personal_codigo = personal_codigo;
    }
    
    
}
