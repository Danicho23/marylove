package models;
public class Parametros {
private int id_par;
private String nombre_par, valores_par;

    public int getId_par() {
        return id_par;
    }

    public void setId_par(int id_par) {
        this.id_par = id_par;
    }

    public String getNombre_par() {
        return nombre_par;
    }

    public void setNombre_par(String nombre_par) {
        this.nombre_par = nombre_par;
    }

    public String getValores_par() {
        return valores_par;
    }

    public void setValores_par(String valores_par) {
        this.valores_par = valores_par;
    }

}
